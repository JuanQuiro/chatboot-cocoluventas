
import { Client } from "ssh2";
import dotenv from "dotenv";
import { join } from "path";
import { fileURLToPath } from "url";
import { dirname } from "path";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
dotenv.config({ path: join(__dirname, ".env") });

const config = {
    host: process.env.VPS_HOST,
    port: parseInt(process.env.VPS_PORT || "22"),
    username: process.env.VPS_USERNAME,
    password: process.env.VPS_PASSWORD,
};

console.log(`Checking Admin User in PocketBase on ${config.host}...`);

const conn = new Client();
conn.on("ready", () => {
    // We can't query PB directly easily without curl or a script. 
    // Let's look for a seed script or try to curl the PB API directly if possible.
    // Assuming PB is on 8090.
    const cmd = `
        echo "--- CHECKING USERS COLLECTION ---"
        curl -v http://localhost:8090/api/collections/users/records?perPage=5
    `;
    conn.exec(cmd, (err, stream) => {
        if (err) throw err;
        stream.on("close", (code: any, signal: any) => {
            conn.end();
        }).on("data", (data: any) => {
            console.log(data.toString());
        }).stderr.on("data", (data: any) => {
            // console.log("STDERR: " + data);
        });
    });
}).on("error", (err) => {
    console.error("Connection Failed:", err);
}).connect(config);
